
# import turtle module
import turtle

# Use turtle module to draw the cross
turtle.showturtle()

turtle.penup()
turtle.forward(50)

turtle.pendown()
turtle.backward(100)
turtle.penup()

turtle.left(90)
turtle.forward(50)
turtle.right(90)
turtle.forward(50)

turtle.pendown()
turtle.right(90)
turtle.forward(100)

turtle.done()
